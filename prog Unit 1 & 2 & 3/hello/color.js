// Import chalk (for latest Node.js versions)
import chalk from 'chalk';

// Using different colors
console.log(chalk.green('Success: Everything is working fine!'));
console.log(chalk.red('Error: Something went wrong!'));
console.log(chalk.yellow('Warning: Be careful!'));
console.log(chalk.blue('Info: This is an information message.'));

// You can also combine styles
console.log(chalk.bold.bgCyan('Node.js + Chalk Demo'));


